    @SuppressWarnings("unchecked")
    private void btnCancelaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelaActionPerformed
        em.getTransaction().rollback();
        em.getTransaction().begin();
        java.util.Collection data = query.getResultList();
        for (Object entity : data) {
            em.refresh(entity);
        }
        list.clear();
        list.addAll(data);
    }//GEN-LAST:event_btnCancelaActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int[] selected = tbBoleta.getSelectedRows();
        List<com.guina.model.Tb014nboletas> toRemove = new ArrayList<com.guina.model.Tb014nboletas>(selected.length);
        for (int idx = 0; idx < selected.length; idx++) {
            com.guina.model.Tb014nboletas t = list.get(tbBoleta.convertRowIndexToModel(selected[idx]));
            toRemove.add(t);
            em.remove(t);
        }
        list.removeAll(toRemove);
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncluirActionPerformed
        com.guina.model.Tb014nboletas t = new com.guina.model.Tb014nboletas();
        em.persist(t);
        list.add(t);
        int row = list.size() - 1;
        tbBoleta.setRowSelectionInterval(row, row);
        tbBoleta.scrollRectToVisible(tbBoleta.getCellRect(row, 0, true));
    }//GEN-LAST:event_btnIncluirActionPerformed
    
    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            em.getTransaction().commit();
            em.getTransaction().begin();
        } catch (RollbackException rex) {
            rex.printStackTrace();
            em.getTransaction().begin();
            List<com.guina.model.Tb014nboletas> merged = new ArrayList<com.guina.model.Tb014nboletas>(list.size());
            for (com.guina.model.Tb014nboletas t : list) {
                merged.add(em.merge(t));
            }
            list.clear();
            list.addAll(merged);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed


