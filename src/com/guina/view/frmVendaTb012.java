    public void MaxId() {
        query = em.createQuery("SELECT t FROM Tb012vendas t ORDER BY t.idvendas DESC");
        List<Tb012vendas> venda = query.getResultList();
        if (venda.size() == 0) {
            txtId.setText("1");
            novo = (Integer.parseInt(txtId.getText()));
        } else if (venda.size() > 0) {
            String args[] = new String[1];
            args[0] = venda.get(0).getIdvendas().toString();
            n = (Integer.parseInt(args[0]));
            txtId.setText(Integer.toString(n));//Recupera o ultimo codigo
            novo = (Integer.parseInt(txtId.getText()) + 1);
        }
    }
