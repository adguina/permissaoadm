    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        txtFlag.setText(jcLoja.getSelectedItem().toString());
        String nome = txtFlag.getText();
        int tam = nome.length();
        nome = txtFlag.getText().substring(0, tam);
        txtFlag.setText(nome);

        //inicia função de Log
        btnNovoLog.doClick();//incia o log
        Dados();//Preenche dados
        User();//Pega Usuario
        btnSalvarLog.doClick();//Salva na tabela de log
        btnCancelaLog.doClick();//Atualiza tabela de Log

        try {
            em.getTransaction().commit();
            em.getTransaction().begin();
        } catch (RollbackException rex) {
            rex.printStackTrace();
            em.getTransaction().begin();
            List<com.guina.model.Tb012vendas> merged = new ArrayList<com.guina.model.Tb012vendas>(list.size());
            for (com.guina.model.Tb012vendas t : list) {
                merged.add(em.merge(t));
            }
            list.clear();
            list.addAll(merged);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

