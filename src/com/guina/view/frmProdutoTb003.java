    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        acao = ("Excluir");
        int[] selected = tbProduto.getSelectedRows();
        List<com.guina.model.Tb003produtos> toRemove = new ArrayList<com.guina.model.Tb003produtos>(selected.length);
        for (int idx = 0; idx < selected.length; idx++) {
            com.guina.model.Tb003produtos t = list.get(tbProduto.convertRowIndexToModel(selected[idx]));
            toRemove.add(t);
            em.remove(t);
        }
        list.removeAll(toRemove);
        btnSalvar.doClick();
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        btnNovoLog.doClick();
        Dados();//Preenche dados
        User();//Pega Usuario
        btnSalvarLog.doClick();//Salva na tabela de log
        btnCancelaLog.doClick();//Atualiza tabela de Log
        
        try {
            em.getTransaction().commit();
            em.getTransaction().begin();
        } catch (RollbackException rex) {
            rex.printStackTrace();
            em.getTransaction().begin();
            List<com.guina.model.Tb003produtos> merged = new ArrayList<com.guina.model.Tb003produtos>(list.size());
            for (com.guina.model.Tb003produtos t : list) {
                merged.add(em.merge(t));
            }
            list.clear();
            list.addAll(merged);
            btnCancela.doClick();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncluirActionPerformed
       //VERIFICA O ULTIMO CODIGO DO PRODUTO
        query = em.createQuery("SELECT t FROM Tb003produtos t ORDER BY t.codigoproduto DESC");
        List<Tb003produtos> produto = query.getResultList();
        if (produto.size() > 0) {
            String args[] = new String[1];
            args[0] = produto.get(0).getCodigoproduto().toString();
            n = (Integer.parseInt(args[0]));
            txtProd.setText(Integer.toString(n));//Recupera o ultimo codigo
            novo = (Integer.parseInt(txtProd.getText())+1);
        }
             
        
        acao = ("Incluir");
        com.guina.model.Tb003produtos t = new com.guina.model.Tb003produtos();
        em.persist(t);
        list.add(t);
        int row = list.size() - 1;
        tbProduto.setRowSelectionInterval(row, row);
        tbProduto.scrollRectToVisible(tbProduto.getCellRect(row, 0, true));
        txtCodigo.setText(String.valueOf(novo));  //Gera um novo codigo
        
        
        
    }//GEN-LAST:event_btnIncluirActionPerformed

    private void btnCancelaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelaActionPerformed
        em.getTransaction().rollback();
        em.getTransaction().begin();
        java.util.Collection data = query.getResultList();
        for (Object entity : data) {
            em.refresh(entity);
        }
        list.clear();
        list.addAll(data);
    }//GEN-LAST:event_btnCancelaActionPerformed

