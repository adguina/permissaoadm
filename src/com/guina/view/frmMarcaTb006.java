    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        acao = ("Excluir");
        int[] selected = tbMarca.getSelectedRows();
        List<com.guina.model.Tb006marcas> toRemove = new ArrayList<com.guina.model.Tb006marcas>(selected.length);
        for (int idx = 0; idx < selected.length; idx++) {
            com.guina.model.Tb006marcas t = list.get(tbMarca.convertRowIndexToModel(selected[idx]));
            toRemove.add(t);
            em.remove(t);
        }
        list.removeAll(toRemove);
    }//GEN-LAST:event_btnExcluirActionPerformed

