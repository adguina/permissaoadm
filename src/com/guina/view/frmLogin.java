    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        //VERIFICA OS USUARIOS LOGADOS E VERIFICA LICENÇA
        tbLog.setRowSelectionInterval(0,0);     
        query = em.createQuery("SELECT t FROM Tb009logados t");
        List<Tb009logados> log = query.getResultList();
        if (log.size() > 0) {
            String args[] = new String[2];
            args[0] = log.get(0).getNumerologado().toString();
            args[1] = log.get(0).getNomelogado().toString();
            n = (Integer.parseInt(args[0]));
            l = (Integer.parseInt(args[1]));
            txtNuser.setText(Integer.toString(n));
            txtLic.setText(Integer.toString(l));
        }
        //SE LOGADOS"n" FOR MAIOR QUE NUMERO DE LICENÇA "l" MANDA MSG
        if (n >= l) {
            JOptionPane.showMessageDialog(this, "Ultrapassou o numero de licenças logados "+l+" \n Pedir pra alguem deslogar!");
       //SE LOGADOS FOR MENOR OU IGUAL O NUMERO QUE LICENÇA FAZ A VERIFCAÇÃO E LOGA NO SISTEMA
        } else if(n < l) {
            query = em.createQuery("SELECT t FROM Tb001user t WHERE t.loginuser = :login and t.senhauser = :senha");
            query.setParameter("login", txtNome.getText());
            query.setParameter("senha", txtSenha.getText());
            List<Tb001user> data = query.getResultList();

            if (data.size() > 0) {
                String args[] = new String[2];
                args[0] = data.get(0).getNomeuser().toString();
                args[1] = data.get(0).getIduser().toString();
                
                //INSERE LOGADO NA TABELA DO SISTEMA, ACRESCENTA tb009logados
                n = n + 1;
                txtNuser.setText(String.valueOf(n));
                btnSalvar.doClick();   
                this.dispose();
                frmPrincipal.main(args);
            } else {
                JOptionPane.showMessageDialog(null, "Usuario ou senha Invalido");
                txtNome.requestFocus();
                //guina  abrir.dispose();
            }
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

