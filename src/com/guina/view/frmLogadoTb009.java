    private void btnCancelaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelaActionPerformed
        em.getTransaction().rollback();
        em.getTransaction().begin();
        java.util.Collection data = query.getResultList();
        for (Object entity : data) {
            em.refresh(entity);
        }
        list.clear();
        list.addAll(data);
    }//GEN-LAST:event_btnCancelaActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int[] selected = tbLogados.getSelectedRows();
        List<com.guina.model.Tb009logados> toRemove = new ArrayList<com.guina.model.Tb009logados>(selected.length);
        for (int idx = 0; idx < selected.length; idx++) {
            com.guina.model.Tb009logados t = list.get(tbLogados.convertRowIndexToModel(selected[idx]));
            toRemove.add(t);
            em.remove(t);
        }
        list.removeAll(toRemove);
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        com.guina.model.Tb009logados t = new com.guina.model.Tb009logados();
        em.persist(t);
        list.add(t);
        int row = list.size() - 1;
        tbLogados.setRowSelectionInterval(row, row);
        tbLogados.scrollRectToVisible(tbLogados.getCellRect(row, 0, true));
    }//GEN-LAST:event_btnInserirActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            em.getTransaction().commit();
            em.getTransaction().begin();
        } catch (RollbackException rex) {
            rex.printStackTrace();
            em.getTransaction().begin();
            List<com.guina.model.Tb009logados> merged = new ArrayList<com.guina.model.Tb009logados>(list.size());
            for (com.guina.model.Tb009logados t : list) {
                merged.add(em.merge(t));
            }
            list.clear();
            list.addAll(merged);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

