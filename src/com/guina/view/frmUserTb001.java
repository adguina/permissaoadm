    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        btnNovoLog.doClick();
        Dados();//Preenche dados
        User();//Pega Usuario
        btnSalvarLog.doClick();//Salva na tabela de log
        btnCancelaLog.doClick();//Atualiza tabela de Log
        
        try {
            em.getTransaction().commit();
            em.getTransaction().begin();
        } catch (RollbackException rex) {
            rex.printStackTrace();
            em.getTransaction().begin();
            List<com.guina.model.Tb001user> merged = new ArrayList<com.guina.model.Tb001user>(list.size());
            for (com.guina.model.Tb001user t : list) {
                merged.add(em.merge(t));
            }
            list.clear();
            list.addAll(merged);
        }
       //atualiza a tab depois que salva
       btnCancela.doClick();

    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnCancelaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelaActionPerformed
        acao = ("Acao Cancelada");
        em.getTransaction().rollback();
        em.getTransaction().begin();
        java.util.Collection data = query.getResultList();
        for (Object entity : data) {
            em.refresh(entity);
        }
        list.clear();
        list.addAll(data);
    }//GEN-LAST:event_btnCancelaActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        acao = ("Delete");
        int[] selected = tbUser.getSelectedRows();
        List<com.guina.model.Tb001user> toRemove = new ArrayList<com.guina.model.Tb001user>(selected.length);
        for (int idx = 0; idx < selected.length; idx++) {
            com.guina.model.Tb001user t = list.get(tbUser.convertRowIndexToModel(selected[idx]));
            toRemove.add(t);
            em.remove(t);
        }
        list.removeAll(toRemove);
        //Depois de deletar Salva
        btnSalvar.doClick();
    }//GEN-LAST:event_btnExcluirActionPerformed

