    public void MaxId() {
        query = em.createQuery("SELECT t FROM Tb013vendedoras t ORDER BY t.idvendedor DESC");
        List<Tb013vendedoras> venda = query.getResultList();
        if (venda.size() == 0) {
            txtId.setText("1");
            novo = (Integer.parseInt(txtId.getText()));
        } else if (venda.size() > 0) {
            String args[] = new String[1];
            args[0] = venda.get(0).getIdvendedor().toString();
            n = (Integer.parseInt(args[0]));
            txtId.setText(Integer.toString(n));//Recupera o ultimo codigo
            novo = (Integer.parseInt(txtId.getText()) + 1);
        }
    }
    
