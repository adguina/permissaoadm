    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        acao = ("Excluir");
        
        int[] selected = tbVendas.getSelectedRows();
        List<com.guina.model.Tb013vendedoras> toRemove = new ArrayList<com.guina.model.Tb013vendedoras>(selected.length);
        for (int idx = 0; idx < selected.length; idx++) {
            com.guina.model.Tb013vendedoras t = list.get(tbVendas.convertRowIndexToModel(selected[idx]));
            toRemove.add(t);
            em.remove(t);
        }
        list.removeAll(toRemove);
         //Depois de deletar Salva
        btnSalvar.doClick();
    }//GEN-LAST:event_btnExcluirActionPerformed

