    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        acao = ("Excluir");
        int[] selected = tbFicha.getSelectedRows();
        List<com.guina.model.Tb019fichas> toRemove = new ArrayList<com.guina.model.Tb019fichas>(selected.length);
        for (int idx = 0; idx < selected.length; idx++) {
            com.guina.model.Tb019fichas t = listFicha.get(tbFicha.convertRowIndexToModel(selected[idx]));
            toRemove.add(t);
            em.remove(t);
        }
        listFicha.removeAll(toRemove);
        //Depois de deletar Salva
        btnSalvar.doClick();
    }//GEN-LAST:event_btnExcluirActionPerformed

