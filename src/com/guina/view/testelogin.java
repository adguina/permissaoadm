    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
            query = em.createQuery("SELECT t FROM Tb001user t WHERE t.loginuser = :login and t.senhauser = :senha");
            query.setParameter("login", txtNome.getText());
            query.setParameter("senha", txtSenha.getText());
            List<Tb001user> data = query.getResultList();

            if (data.size() > 0) {
                String args[] = new String[2];
                args[0] = data.get(0).getNomeuser().toString();
                args[1] = data.get(0).getIduser().toString();

                //INSERE LOGADO NA TABELA DO SISTEMA, ACRESCENTA tb009logados
                this.dispose();
                frmPrincipal.main(args);
            } else {
                JOptionPane.showMessageDialog(null, "Usuario ou senha Invalido");
                txtNome.requestFocus();
                //guina  abrir.dispose();
            }
        
    }//GEN-LAST:event_btnEntrarActionPerformed

