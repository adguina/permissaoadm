    public void MaxId() {
        query = em.createQuery("SELECT t FROM Tb014nboletas t ORDER BY t.idboleta DESC");
        List<Tb014nboletas> boleta = query.getResultList();
        if (boleta.size() == 0) {
            txtId.setText("1");
            novo = (Integer.parseInt(txtId.getText()));
        } else if (boleta.size() > 0) {
            String args[] = new String[1];
            args[0] = boleta.get(0).getIdboleta().toString();
            n = (Integer.parseInt(args[0]));
            txtId.setText(Integer.toString(n));//Recupera o ultimo codigo
            novo = (Integer.parseInt(txtId.getText()) + 1);
        }
    }

